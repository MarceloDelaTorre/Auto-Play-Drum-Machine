function keepPlayigMusic() {
  if (keepPlayig === false) {
    play([])
    return;
  }
  return play(sequencing);
}

function play(defaultSequecing) {
  defaultSequecing.forEach((arr, i) => {
    setTimeout(() => {
      if (i > 15) {
        keepPlayigMusic();
      }
      $(".shadowPlay").removeClass("shadowPlay");
      arr.forEach(
        (id) => $("#" + id).addClass("shadowPlay")
      ), makeSound();;
    }, i * 310);
  });
}


